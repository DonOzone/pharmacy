       	<span class="black"><a name="allergy" id="allergy"></a>What is allergy?</span><br/>
       	<p><br />
       	  Allergies are abnormal reactions to ordinarily harmless substances. The sensitizing substances, called allergens, may be inhaled, swallowed, or come into contact with the skin.</p>
       	<p>Allergens that most frequently cause problems are: pollens, mold spores, house dust mites, animal danders, foods, insect bites or stings, plants, insect spores, latex rubber, viruses, bacteria, medications and environmental conditions (such as cold temperatures). Allergic reactions can occur in one area, such as sneezing or a skin rash or sneezing, or may include more than one symptom.</p>
        
        <p><span class="black"><a name="causes" id="causes"></a>What Causes Allergies?</span><br />
          Allergies are relatively common. Both genetics and environmental factors play a role.<br />
          The immune system normally protects the body against harmful substances, such as bacteria and viruses. It also reacts to foreign substances called allergens, which are generally harmless and in most people do not cause a problem.<br />
          But in a person with allergies, the immune response is oversensitive. When it recognizes an allergen, it releases chemicals such as histamines. which fight off the allergen. This causes itching, swelling, mucus production, muscle spasms, hives, rashes, and other symptoms, which vary from person to person.<br />
          Common allergens include pollen, mold, pet dander, and dust. Food and drug allergies are common. Allergic reactions can also be caused by insect bites, jewelry, cosmetics, spices, and other substances<br />
          Some people have allergy-like reactions to hot or cold temperatures, sunlight, or other environmental triggers. Sometimes, friction (rubbing or roughly stroking the skin) will cause symptoms.<br />
          A specific allergy is not usually passed down through families (inherited). However, if both your parents have allergies, you are likely to have allergies. The chance is greater if your mother has allergies.</p>
        <p>Allergies may make certain medical conditions such as sinus problems, eczema, and asthma worse..<br /></p>
        </p>
        
