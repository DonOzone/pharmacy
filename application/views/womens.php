          <p>Women’s Health<br />
          Let's face it: Starting a fitness regimen is easy. It's sticking to it that's tough. One recent study found that about half of all Americans don't get enough exercise. What keeps the other half going? Their secret is that they've found ways to stay motivated. Here's how you can, too:</p>
          <p>Make a contract with yourself.</p>
          <p>The simple act of putting your fitness goals down on paper gives them weight. Make them specific and realistic. &quot;I will run three times a week, for 30 minutes at a time,&quot; is much better than &quot;I will get more exercise.&quot; Be careful not to set unrealistic goals that will lead to disappointment and frustration. Exercise will make you healthier and give you a certain glow, but it won't fix your love life or turn you into a supermodel.</p>
          <p>Plan ahead.</p>
          <p>Most people, especially women, don't set aside regular time for exercise. In fact, the number one reason why people don't exercise is because they're too busy doing things for other people -- their spouse, their kids, their boss. How do you make time? By planning for all the little stumbling blocks that can get in the way. Remember, it's okay to take time for yourself. Make a schedule and stick to it, or incorporate exercise into your daily routine. Try biking to work or walking to do errands instead of taking the bus or driving. You'll be surprised by how easily physical activity can become part of your everyday life.</p>
          <p>Make it fun.</p>
          <p>To stick with an exercise plan, you have to enjoy it. Here's how:</p>
          <p>•Make exercise a social event. Join a group or find a workout partner; exercising with others helps keep your spirits up and makes particular sense for activities that work best with two people, such as weight training. Playing catch or kick the can with your kids counts as exercise, too. Try taking a walk or bike ride with your family, or spend the afternoon together at the pool.<br />
            •Vary your routine. Athletes refer to this as cross-training; working several activities into your week will help you develop different sets of muscles and skills. Besides, you don't have to be training for a triathlon to enjoy the benefits of mixing different sports, such as running, biking, and swimming.<br />
            •Keep your brain busy, too. Try watching TV while on the treadmill, reading while pedaling the stationary bike, or listening to music or books on tape when you're using the weight machines. Use your regular walk or jog as a people-watching expedition or to view scenery you particularly like.</p>
